    private void login(){
         try {
            c.rs=c.stmt.executeQuery("select status,el.eid from EmployeeLogin   el inner join employee e on e.eid=el.eid where username='"+user.getText()
                +"' and  password='"+String.valueOf(pass.getPassword())+"';");
            if(c.rs.next()&&c.rs.getString("status").equals("Working")){
                EmployeeMenu detail=new EmployeeMenu(c.rs.getString("eid"));
                detail.setVisible(true);
                this.setVisible(false);
            }
            else
            JOptionPane.showMessageDialog(null, "Incorrect username or password.\nNot Registered yet? Register.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "An Error Ocurred.\nError Description - "+ex);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Something unexpected Occured.\n Contact Admin for Support.\nError Description - "+ex);
            new logs.loggWriter().write2("Something unexpected Occured.\n Contact Admin for Support.\nError Description - "+ex);
        }
    }
