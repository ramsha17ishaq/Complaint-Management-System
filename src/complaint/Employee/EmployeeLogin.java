    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        String user=JOptionPane.showInputDialog("Enter Username :").toString();
        try {
            c.rs=c.stmt.executeQuery("select *  from Employee e inner join employeelogin el on el.eid=e.eid  where el.username='"+user+"';");
            c.rs.next();
            String ans=JOptionPane.showInputDialog("Enter Employee ID - ");
            if(ans.equals(c.rs.getString("el.eid")))
            c.stmt.executeUpdate("Update EmployeeLogin set Password='"+JOptionPane.showInputDialog("Enter New Password :").toString()+"' where username='"+user+"';");
            else
            JOptionPane.showMessageDialog(null, "Employee ID doesn't match.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error Connecting To The Database.\nError Description - "+ex);
        }
    }//GEN-LAST:event_jLabel5MouseClicked

